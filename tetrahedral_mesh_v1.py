# SECTION 5: MESH QUALITY METRICS
# ============================================================
class MeshQualityAnalyzer:
    """
    FEA mesh quality metrics per tetrahedron.

    Metrics:
    - Volume (mm³)
    - Aspect Ratio: circumradius/inradius ratio (ideal = 1)
    - Scaled Jacobian: element distortion [-1, 1] (ideal = 1)
    - Skewness: deviation from ideal [0, 1] (ideal = 0)
    - Edge Ratio: max/min edge length (ideal = 1)
    - Min Dihedral Angle: smallest angle between faces
    """

    @staticmethod
    def compute_all_metrics(nodes_df, elements):
        """Compute quality metrics for all tetrahedra. Returns DataFrame."""
        if not elements or nodes_df.empty:
            return pd.DataFrame()

        nid_to_xyz = {}
        for _, row in nodes_df.iterrows():
            nid_to_xyz[int(row['node_id'])] = np.array([row['x'], row['y'], row['z']])

        records = []
        for ei, elem in enumerate(elements):
            try:
                pts = [nid_to_xyz[nid] for nid in elem[:4] if nid in nid_to_xyz]
                if len(pts) < 4:
                    continue
                m = MeshQualityAnalyzer._tet_metrics(*pts)
                if m:
                    m['elem_idx'] = ei
                    m['n0'], m['n1'], m['n2'], m['n3'] = elem[:4]
                    records.append(m)
            except (KeyError, ValueError):
                continue

        if not records:
            return pd.DataFrame()

        df = pd.DataFrame(records)
        df['quality_class'] = df.apply(
            lambda r: MeshQualityAnalyzer._classify(
                r['aspect_ratio'], r['scaled_jacobian'], r['skewness']), axis=1)
        return df

    @staticmethod
    def _tet_metrics(p0, p1, p2, p3):
        """Compute quality metrics for a single tetrahedron."""
        edges = [p1 - p0, p2 - p0, p3 - p0, p2 - p1, p3 - p1, p3 - p2]
        edge_lengths = np.array([np.linalg.norm(e) for e in edges])
        if np.any(edge_lengths < 1e-12):
            return None

        v0, v1, v2 = p1 - p0, p2 - p0, p3 - p0
        det_J = np.dot(v0, np.cross(v1, v2))
        volume = abs(det_J) / 6.0
        if volume < 1e-15:
            return None

        edge_ratio = edge_lengths.max() / edge_lengths.min()

        faces = [(p0, p1, p2), (p0, p1, p3), (p0, p2, p3), (p1, p2, p3)]
        face_areas = [0.5 * np.linalg.norm(np.cross(fb - fa, fc - fa))
                      for fa, fb, fc in faces]
        total_area = sum(face_areas)
        if total_area < 1e-15:
            return None

        inradius = 3.0 * volume / total_area
        aspect_ratio = edge_lengths.max() / (2.0 * np.sqrt(6) * inradius)

        l0, l1, l2 = np.linalg.norm(v0), np.linalg.norm(v1), np.linalg.norm(v2)
        if l0 * l1 * l2 < 1e-15:
            return None
        scaled_jac = np.clip(det_J / (l0 * l1 * l2 * np.sqrt(2)), -1.0, 1.0)

        mean_edge = edge_lengths.mean()
        v_ideal = mean_edge ** 3 / (6.0 * np.sqrt(2))
        skewness = 1.0 - min(volume / v_ideal, 1.0) if v_ideal > 0 else 1.0

        normals = [np.cross(fb - fa, fc - fa) for fa, fb, fc in faces]
        normals = [n / max(np.linalg.norm(n), 1e-12) for n in normals]
        angles = []
        for i in range(len(normals)):
            for j in range(i + 1, len(normals)):
                angles.append(np.degrees(np.arccos(np.clip(abs(np.dot(normals[i], normals[j])), -1, 1))))
        min_angle = min(angles) if angles else 0

        return {'volume': volume, 'aspect_ratio': aspect_ratio,
                'scaled_jacobian': scaled_jac, 'skewness': skewness,
                'edge_ratio': edge_ratio, 'min_angle': min_angle}

    @staticmethod
    def _classify(ar, sj, skew):
        """Classify element quality based on standard FEA thresholds."""
        votes = [0, 0, 0]
        if ar < CONFIG['ar_good']:   votes[0] += 1
        elif ar > CONFIG['ar_poor']: votes[2] += 1
        else:                        votes[1] += 1
        if sj > CONFIG['sj_good']:   votes[0] += 1
        elif sj < CONFIG['sj_poor']: votes[2] += 1
        else:                        votes[1] += 1
        if skew < CONFIG['skew_good']:  votes[0] += 1
        elif skew > CONFIG['skew_poor']:votes[2] += 1
        else:                           votes[1] += 1
        return ['good', 'acceptable', 'poor'][np.argmax(votes)]


