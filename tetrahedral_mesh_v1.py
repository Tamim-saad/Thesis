class Trainer:
    """Training pipeline for SurfaceToVolumeCVAE."""

    def __init__(self, model, train_loader, val_loader):
        self.model = model.to(DEVICE)
        self.criterion = MeshGenerationLoss()
        self.optimizer = torch.optim.AdamW(
            model.parameters(), lr=MODEL_CONFIG['lr'],
            weight_decay=MODEL_CONFIG['weight_decay'])
        self.scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(
            self.optimizer, patience=MODEL_CONFIG['lr_patience'],
            factor=0.5, min_lr=1e-6)
        self.train_loader = train_loader
        self.val_loader = val_loader
        self.history = {'train_loss': [], 'val_loss': [],
                        'train_cd': [], 'val_cd': []}

    def train_epoch(self):
        self.model.train()
        total_loss, total_cd, n = 0, 0, 0
        for surface, interior, _ in self.train_loader:
            surface = surface.to(DEVICE)
            interior = interior.to(DEVICE)

            pred, mu, logvar = self.model(surface)
            loss, details = self.criterion(pred, interior, mu, logvar)

            self.optimizer.zero_grad()
            loss.backward()
            torch.nn.utils.clip_grad_norm_(self.model.parameters(), 1.0)
            self.optimizer.step()

            total_loss += details['total'] * surface.size(0)
            total_cd += details['chamfer'] * surface.size(0)
            n += surface.size(0)

        return total_loss / n, total_cd / n

    @torch.no_grad()
    def validate_epoch(self):
        self.model.eval()
        total_loss, total_cd, n = 0, 0, 0
        for surface, interior, _ in self.val_loader:
            surface = surface.to(DEVICE)
            interior = interior.to(DEVICE)

            pred, mu, logvar = self.model(surface)
            loss, details = self.criterion(pred, interior, mu, logvar)

            total_loss += details['total'] * surface.size(0)
            total_cd += details['chamfer'] * surface.size(0)
            n += surface.size(0)

        return total_loss / n, total_cd / n

    def train(self):
        best_val = float('inf')
        patience_counter = 0
        best_state = None

        print(f"\n  Training on {DEVICE} | "
              f"{sum(p.numel() for p in self.model.parameters()):,} parameters")

        for epoch in range(MODEL_CONFIG['epochs']):
            train_loss, train_cd = self.train_epoch()
            val_loss, val_cd = self.validate_epoch()

            self.history['train_loss'].append(train_loss)
            self.history['val_loss'].append(val_loss)
            self.history['train_cd'].append(train_cd)
            self.history['val_cd'].append(val_cd)

            self.scheduler.step(val_loss)

            if val_loss < best_val:
                best_val = val_loss
                patience_counter = 0
                best_state = {k: v.cpu().clone()
                              for k, v in self.model.state_dict().items()}
            else:
                patience_counter += 1

            if (epoch + 1) % 10 == 0 or epoch == 0:
                lr = self.optimizer.param_groups[0]['lr']
                print(f"    Epoch {epoch+1:3d} | Train CD: {train_cd:.6f} | "
                      f"Val CD: {val_cd:.6f} | LR: {lr:.1e}")

            if patience_counter >= 30:
                print(f"    Early stopping at epoch {epoch+1}")
                break

        if best_state:
            self.model.load_state_dict(best_state)
        return self.history


def run_kfold_training(all_data):
    """K-Fold cross-validation training."""
    print("\n" + "=" * 60)
    print("🧠 K-FOLD CROSS-VALIDATION TRAINING")
    print("=" * 60)

    # Prepare full dataset (no augmentation for splitting)
    full_dataset = TetrahedralMeshDataset(all_data, augment=False)
    n_samples = len(full_dataset)

    if n_samples < MODEL_CONFIG['k_folds']:
        print(f"  ⚠️ Only {n_samples} samples, reducing folds")
        MODEL_CONFIG['k_folds'] = min(n_samples, 3)

    kf = KFold(n_splits=MODEL_CONFIG['k_folds'], shuffle=True, random_state=42)
    indices = list(range(n_samples))

    all_fold_results = []

    for fold, (train_idx, val_idx) in enumerate(kf.split(indices)):
        print(f"\n{'='*50}")
        print(f"  FOLD {fold+1}/{MODEL_CONFIG['k_folds']} "
              f"(train={len(train_idx)}, val={len(val_idx)})")
        print(f"{'='*50}")

        # Create fold datasets
        train_ds = TetrahedralMeshDataset.__new__(TetrahedralMeshDataset)
        train_ds.samples = [full_dataset.samples[i] for i in train_idx]
        train_ds.filenames = [full_dataset.filenames[i] for i in train_idx]
        train_ds.augment = True

        val_ds = TetrahedralMeshDataset.__new__(TetrahedralMeshDataset)
        val_ds.samples = [full_dataset.samples[i] for i in val_idx]
        val_ds.filenames = [full_dataset.filenames[i] for i in val_idx]
        val_ds.augment = False

        train_loader = DataLoader(train_ds, MODEL_CONFIG['batch_size'],
                                  shuffle=True, drop_last=True)
        val_loader = DataLoader(val_ds, MODEL_CONFIG['batch_size'])

        # Create model
        model = SurfaceToVolumeCVAE()
        trainer = Trainer(model, train_loader, val_loader)
        history = trainer.train()

        # Evaluate on validation set
        fold_metrics = evaluate_fold(model, val_ds)
        fold_metrics['history'] = history
        fold_metrics['fold'] = fold + 1
        all_fold_results.append(fold_metrics)

    # Print summary
    print("\n" + "=" * 60)
    print("📊 K-FOLD RESULTS SUMMARY")
    print("=" * 60)
    val_cds = [r['mean_chamfer'] for r in all_fold_results]
    print(f"  Chamfer Distance: {np.mean(val_cds):.6f} ± {np.std(val_cds):.6f}")

    if all(r.get('mean_ar') is not None for r in all_fold_results):
        val_ars = [r['mean_ar'] for r in all_fold_results]
        val_sjs = [r['mean_sj'] for r in all_fold_results]
        val_goods = [r['pct_good'] for r in all_fold_results]
        print(f"  Aspect Ratio:     {np.mean(val_ars):.3f} ± {np.std(val_ars):.3f}")
        print(f"  Scaled Jacobian:  {np.mean(val_sjs):.3f} ± {np.std(val_sjs):.3f}")
        print(f"  % Good quality:   {np.mean(val_goods):.1f}% ± {np.std(val_goods):.1f}%")

    return all_fold_results


@torch.no_grad()
def evaluate_fold(model, val_ds):
    """Evaluate a trained model on validation set."""
    model.eval()
    evaluator = MeshGenerationEvaluator()
    all_metrics = []

    for i in range(len(val_ds)):
        sample = val_ds.samples[i]
        surface = torch.tensor(sample['surface']).unsqueeze(0).to(DEVICE)
        pred, _, _ = model(surface)
        pred_np = pred.cpu().numpy()[0]

        metrics = evaluator.evaluate_generated_mesh(
            pred_np, sample['interior'], sample['surface'],
            sample['centroid'], sample['scale'])
        all_metrics.append(metrics)

    result = {'mean_chamfer': np.mean([m['chamfer_distance'] for m in all_metrics])}
    if all('mean_aspect_ratio' in m for m in all_metrics):
        result['mean_ar'] = np.mean([m['mean_aspect_ratio'] for m in all_metrics])
        result['mean_sj'] = np.mean([m['mean_scaled_jacobian'] for m in all_metrics])
        result['pct_good'] = np.mean([m.get('pct_good', 0) for m in all_metrics])
    result['all_metrics'] = all_metrics
    return result


