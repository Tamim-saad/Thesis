class TetrahedralMeshDataset(Dataset):
    """
    PyTorch Dataset for (surface → interior nodes) pairs.

    Each sample: surface point cloud (input) → interior point cloud (target).
    Data augmentation: random rotation, jitter, scaling for small dataset.
    """

    def __init__(self, all_data, augment=False):
        self.samples = []
        self.augment = augment
        self.filenames = []

        for fname, data in all_data.items():
            pair = MeshRepresentation.prepare_training_pair(
                data['nodes_df'], data['elements'])
            if pair is not None:
                self.samples.append(pair)
                self.filenames.append(fname)

        print(f"  Dataset: {len(self.samples)} valid samples "
              f"({'augmented' if augment else 'no augment'})")

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        sample = self.samples[idx]
        surface = torch.tensor(sample['surface'])
        interior = torch.tensor(sample['interior'])

        if self.augment:
            surface, interior = self._augment(surface, interior)

        return surface, interior, idx

    def _augment(self, surface, interior):
        """Data augmentation for small dataset (198 samples)."""
        # Random rotation around Z-axis (anatomically meaningful)
        angle = np.random.uniform(0, 2 * np.pi)
        cos_a, sin_a = np.cos(angle), np.sin(angle)
        rot = torch.tensor([
            [cos_a, -sin_a, 0],
            [sin_a,  cos_a, 0],
            [0,      0,     1]
        ], dtype=torch.float32)
        surface = surface @ rot.T
        interior = interior @ rot.T

        # Random scaling (±5%)
        s = 1.0 + np.random.uniform(-0.05, 0.05)
        surface = surface * s
        interior = interior * s

        # Random jitter on surface only (small noise)
        surface = surface + torch.randn_like(surface) * 0.002

        return surface, interior


