# SECTION 12: MODEL — CVAE DECODER (Interior Node Generation)
# ============================================================
class InteriorNodeDecoder(nn.Module):
    """
    Conditional decoder: latent vector → interior node positions.

    Uses FoldingNet-inspired approach: deforms a template point distribution
    conditioned on surface features to predict interior node locations.
    """

    def __init__(self, latent_dim=512, condition_dim=1024, n_points=4096):
        super().__init__()
        self.n_points = n_points

        # Generate template grid in 3D (initial point distribution)
        self.register_buffer('template',
            self._create_3d_template(n_points))

        # Folding network: template + condition → deformed points
        total_in = 3 + latent_dim + condition_dim
        self.fold1 = nn.Sequential(
            nn.Linear(total_in, 512), nn.ReLU(),
            nn.Linear(512, 512), nn.ReLU(),
            nn.Linear(512, 256), nn.ReLU(),
            nn.Linear(256, 3))

        self.fold2 = nn.Sequential(
            nn.Linear(3 + latent_dim + condition_dim, 512), nn.ReLU(),
            nn.Linear(512, 512), nn.ReLU(),
            nn.Linear(512, 256), nn.ReLU(),
            nn.Linear(256, 3))

    def _create_3d_template(self, n_points):
        """Create initial 3D point distribution (uniform in unit sphere)."""
        points = []
        while len(points) < n_points:
            p = np.random.randn(n_points * 2, 3)
            norms = np.linalg.norm(p, axis=1, keepdims=True)
            p = p / norms  # on unit sphere
            r = np.random.uniform(0, 1, (len(p), 1)) ** (1/3)  # uniform in volume
            p = p * r
            points.extend(p.tolist())
        points = np.array(points[:n_points], dtype=np.float32)
        return torch.tensor(points)

    def forward(self, z, condition):
        """
        z: (B, latent_dim) sampled latent
        condition: (B, condition_dim) surface features
        returns: (B, n_points, 3) predicted interior positions
        """
        B = z.size(0)
        template = self.template.unsqueeze(0).expand(B, -1, -1)  # (B, N, 3)

        # Expand z and condition to match points
        z_exp = z.unsqueeze(1).expand(-1, self.n_points, -1)
        c_exp = condition.unsqueeze(1).expand(-1, self.n_points, -1)

        # First folding
        inp1 = torch.cat([template, z_exp, c_exp], dim=2)
        fold1_out = self.fold1(inp1)

        # Second folding (refinement)
        inp2 = torch.cat([fold1_out, z_exp, c_exp], dim=2)
        fold2_out = self.fold2(inp2)

        return fold2_out


class SurfaceToVolumeCVAE(nn.Module):
    """
    Conditional Variational Autoencoder for mesh generation.

    Architecture (2024-2025 SOTA components):
    - Encoder: DGCNN (captures local surface geometry via dynamic graph)
    - Latent: VAE with surface-conditioned prior (handles 198 sample dataset)
    - Decoder: FoldingNet-style (deforms template → interior points)

    Pipeline:
    Surface points → DGCNN → surface features
                           → μ, σ (VAE latent)
                           → sample z
                           → Decoder(z, surface_features) → interior points
    """

    def __init__(self):
        super().__init__()
        k = MODEL_CONFIG['dgcnn_k']
        latent_dim = MODEL_CONFIG['latent_dim']
        n_interior = MODEL_CONFIG['n_interior_pts']
        encoder_out = 512 * 2  # DGCNN outputs max+avg pooling

        # Surface encoder (DGCNN — SOTA for point cloud feature extraction)
        self.encoder = DGCNNEncoder(input_dim=3, output_dim=512, k=k)

        # VAE latent space
        self.fc_mu = nn.Linear(encoder_out, latent_dim)
        self.fc_logvar = nn.Linear(encoder_out, latent_dim)

        # Interior point decoder (FoldingNet-style)
        self.decoder = InteriorNodeDecoder(
            latent_dim=latent_dim, condition_dim=encoder_out,
            n_points=n_interior)

    def encode(self, surface_pts):
        """Encode surface → features + latent distribution."""
        # surface_pts: (B, N, 3) → transpose to (B, 3, N) for DGCNN
        x = surface_pts.transpose(1, 2)
        features = self.encoder(x)  # (B, 1024)
        mu = self.fc_mu(features)
        logvar = self.fc_logvar(features)
        return features, mu, logvar

    def reparameterize(self, mu, logvar):
        """VAE reparameterization trick."""
        if self.training:
            std = torch.exp(0.5 * logvar)
            eps = torch.randn_like(std)
            return mu + eps * std
        return mu

    def decode(self, z, condition):
        """Decode latent + condition → interior points."""
        return self.decoder(z, condition)

    def forward(self, surface_pts):
        """Full forward: surface → interior points."""
        features, mu, logvar = self.encode(surface_pts)
        z = self.reparameterize(mu, logvar)
        interior_pred = self.decode(z, features)
        return interior_pred, mu, logvar


