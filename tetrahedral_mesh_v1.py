class MeshVisualizer:
    """
    Visualize tetrahedral FE mesh — wireframe, surface, quality maps.
    """

    @staticmethod
    def plot_surface_mesh(nodes_df, surface_faces, title="Femur Surface Mesh", ax=None):
        """Plot triangulated surface mesh using matplotlib."""
        if ax is None:
            fig = plt.figure(figsize=(12, 10))
            ax = fig.add_subplot(111, projection='3d')

        nid_to_xyz = {}
        for _, row in nodes_df.iterrows():
            nid_to_xyz[int(row['node_id'])] = np.array([row['x'], row['y'], row['z']])

        triangles = []
        for face in surface_faces:
            pts = [nid_to_xyz.get(nid) for nid in face]
            if None not in pts:
                triangles.append(pts)

        if not triangles:
            print("⚠️ No valid triangles")
            return ax

        max_tris = CONFIG['max_viz_elements']
        if len(triangles) > max_tris:
            idx = np.random.choice(len(triangles), max_tris, replace=False)
            triangles = [triangles[i] for i in idx]

        poly = Poly3DCollection(triangles, alpha=0.3, linewidths=0.2,
                                edgecolors='steelblue')
        poly.set_facecolor('lightcyan')
        ax.add_collection3d(poly)

        all_pts = np.array([p for tri in triangles for p in tri])
        margin = 5
        ax.set_xlim([all_pts[:, 0].min() - margin, all_pts[:, 0].max() + margin])
        ax.set_ylim([all_pts[:, 1].min() - margin, all_pts[:, 1].max() + margin])
        ax.set_zlim([all_pts[:, 2].min() - margin, all_pts[:, 2].max() + margin])
        ax.set_xlabel('X (mm)'); ax.set_ylabel('Y (mm)'); ax.set_zlabel('Z (mm)')
        ax.set_title(title, fontsize=14, fontweight='bold')
        return ax

    @staticmethod
    def plot_surface_plotly(nodes_df, surface_faces, title="Femur Surface Mesh"):
        """Interactive 3D surface mesh with Plotly."""
        if not USE_PLOTLY:
            return MeshVisualizer.plot_surface_mesh(nodes_df, surface_faces, title)

        nid_to_xyz = {}
        for _, row in nodes_df.iterrows():
            nid_to_xyz[int(row['node_id'])] = [row['x'], row['y'], row['z']]

        surface_nids = set()
        for face in surface_faces:
            surface_nids.update(face)

        nid_to_idx = {}
        vertices = []
        for idx, nid in enumerate(sorted(surface_nids)):
            if nid in nid_to_xyz:
                nid_to_idx[nid] = idx
                vertices.append(nid_to_xyz[nid])
        vertices = np.array(vertices)

