# SECTION 3: CDB PARSER (NBLOCK + EBLOCK)
# ============================================================
class CDBParser:
    """
    Parse ANSYS CDB files to extract:
      - NBLOCK: Node coordinates (node_id, x, y, z)
      - EBLOCK: Tetrahedral element connectivity (4 node IDs per element)

    CDB files are generated by lhpOpExporterAnsysCDB from QCT-based FE models.
    Each file = one femur (left/right) from a patient.
    Naming: PatientID_left/right_bonemat.cdb
    """

    def __init__(self):
        self.all_data = {}

    def parse_file(self, file_path):
        """Parse a single CDB file. Try pyansys first, fallback to custom."""
        fname = os.path.basename(file_path)
        if USE_PYANSYS:
            try:
                return self._parse_pyansys(file_path, fname)
            except Exception as e:
                print(f"  ⚠️ pyansys failed for {fname}: {e}, using custom parser")
        return self._parse_custom(file_path, fname)

    def _parse_pyansys(self, file_path, fname):
        """Parse using ansys-mapdl-reader (official)."""
        archive = Archive(file_path)
        node_ids = archive.nnum
        nodes_arr = archive.nodes
        nodes_df = pd.DataFrame({
            'node_id': node_ids,
            'x': nodes_arr[:, 0], 'y': nodes_arr[:, 1], 'z': nodes_arr[:, 2]
        })
        elements = []
        if hasattr(archive, 'elem') and len(archive.elem) > 0:
            for elem in archive.elem:
                node_list = [n for n in elem[-1] if n > 0]
                if len(node_list) >= 4:
                    elements.append(tuple(node_list[:4]))

        metadata = self._build_metadata(fname, file_path, 'pyansys')
        print(f"  ✅ {fname}: {len(nodes_df)} nodes, {len(elements)} tetrahedra (pyansys)")
        return nodes_df, elements, metadata

    def _parse_custom(self, file_path, fname):
        """Custom parser for CDB files (robust fallback)."""
        nodes, elements = [], []
        in_nblock, in_eblock = False, False
        format_info = None

        with open(file_path, 'r', encoding='utf-8', errors='ignore') as f:
            lines = f.readlines()

        for i, line in enumerate(lines):
            ls = line.strip()

            # --- NBLOCK parsing ---
            if ls.startswith('NBLOCK'):
                in_nblock, in_eblock = True, False
                continue
            if in_nblock and ls.startswith('('):
                fmt = {}
                im = re.search(r'(\d+)i(\d+)', ls)
                if im:
                    fmt['iw'] = int(im.group(2))
                fm = re.search(r'(\d+)e(\d+)', ls)
                if fm:
                    fmt['fw'] = int(fm.group(2))
                format_info = fmt if fmt else None
                continue
            if in_nblock and ls and not ls.startswith('!') and not ls.startswith('/'):
                if ls.startswith('EBLOCK') or ls.startswith('-1') or ls.startswith('N,'):
                    in_nblock = False
                    if ls.startswith('EBLOCK'):
                        in_eblock = True
                    continue
                result = self._parse_node_line(line, ls, format_info)
                if result:
                    nodes.append(result)
                continue

            # --- EBLOCK parsing ---
            if ls.startswith('EBLOCK'):
                in_eblock, in_nblock = True, False
                continue
            if in_eblock and ls.startswith('('):
                continue
            if in_eblock:
                if ls.startswith('-1') or ls.startswith('NBLOCK') or \
                   ls.startswith('FINISH') or not ls:
                    in_eblock = False
                    continue
                elem = self._parse_element_line(ls)
                if elem:
                    elements.append(elem)

        nodes_df = pd.DataFrame(nodes, columns=['node_id', 'x', 'y', 'z']) if nodes else pd.DataFrame()
        metadata = self._build_metadata(fname, file_path, 'custom')
        print(f"  ✅ {fname}: {len(nodes_df)} nodes, {len(elements)} tetrahedra (custom)")
        return nodes_df, elements, metadata

    def _parse_node_line(self, raw_line, stripped, fmt):
        """Parse a single NBLOCK line (fixed-width or space-separated)."""
        if fmt and 'iw' in fmt and 'fw' in fmt:
            try:
                iw, fw = fmt['iw'], fmt['fw']
                offset = iw * 3
                nid = int(raw_line[:iw].strip())
                x = float(raw_line[offset:offset + fw].strip())
                y = float(raw_line[offset + fw:offset + 2 * fw].strip())
                z = float(raw_line[offset + 2 * fw:offset + 3 * fw].strip())
                return [nid, x, y, z]
            except (ValueError, IndexError):
                pass
        try:
            parts = stripped.split()
            if len(parts) >= 6:
                return [int(parts[0]), float(parts[3]), float(parts[4]), float(parts[5])]
        except (ValueError, IndexError):
            pass
        return None

    def _parse_element_line(self, line):
        """
        Parse EBLOCK line → tetrahedral element (4 node IDs).

        EBLOCK format: first 11 fields = metadata, field 9 = num_nodes,
        fields 12+ = node IDs.
        """
        try:
            parts = line.split()
            if len(parts) >= 11:
                num_nodes = int(parts[8]) if parts[8].isdigit() else 4
                node_start = 11
                if len(parts) >= node_start + 4:
                    node_ids = []
                    for j in range(min(num_nodes, len(parts) - node_start)):
                        p = parts[node_start + j]
                        if p.lstrip('-').isdigit() and int(p) > 0:
                            node_ids.append(int(p))
                    if len(node_ids) >= 4:
                        return tuple(node_ids[:4])
            elif len(parts) >= 8:
                node_ids = [int(p) for p in parts if p.lstrip('-').isdigit() and int(p) > 0]
                if len(node_ids) >= 4:
                    return tuple(node_ids[-4:])
        except (ValueError, IndexError):
            pass
        return None

    def _build_metadata(self, fname, file_path, parser_type):
        """Build metadata dict from filename."""
        parts = fname.replace('_bonemat.cdb', '').replace('_re', '').split('_')
        patient_id = parts[0] if parts else fname
        side = 'left' if 'left' in fname.lower() else ('right' if 'right' in fname.lower() else 'unknown')
        return {'parser': parser_type, 'file_path': file_path,
                'patient_id': patient_id, 'side': side}

    def parse_directory(self, directory):
        """Parse all CDB files in a directory."""
        cdb_files = sorted(glob.glob(os.path.join(directory, "*.cdb")))
        if not cdb_files:
            print(f"❌ No CDB files found in {directory}")
            return {}
        print(f"🔍 Found {len(cdb_files)} CDB files in {directory}")
        print("-" * 60)

        for fp in cdb_files:
            fname = os.path.basename(fp)
            nodes_df, elements, metadata = self.parse_file(fp)
            if not nodes_df.empty:
                self.all_data[fname] = {
                    'nodes_df': nodes_df, 'elements': elements, 'metadata': metadata
                }

        total_nodes = sum(len(d['nodes_df']) for d in self.all_data.values())
        total_elems = sum(len(d['elements']) for d in self.all_data.values())
        n_with_elem = sum(1 for d in self.all_data.values() if len(d['elements']) > 0)
        n_left = sum(1 for d in self.all_data.values() if d['metadata']['side'] == 'left')
        n_right = sum(1 for d in self.all_data.values() if d['metadata']['side'] == 'right')

        print("\n" + "=" * 60)
        print(f"📊 PARSING SUMMARY")
        print(f"  Files parsed:     {len(self.all_data)}")
        print(f"  With elements:    {n_with_elem}")
        print(f"  Left / Right:     {n_left} / {n_right}")
        print(f"  Total nodes:      {total_nodes:,}")
        print(f"  Total tetrahedra: {total_elems:,}")
        print(f"  Avg nodes/file:   {total_nodes // max(len(self.all_data), 1):,}")
        print(f"  Avg elems/file:   {total_elems // max(len(self.all_data), 1):,}")
        print("=" * 60)
        return self.all_data


