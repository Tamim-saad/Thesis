# SECTION 4: TETRAHEDRAL MESH ANALYSIS
# ============================================================
class TetrahedralMeshAnalyzer:
    """
    Analyze tetrahedral mesh structure from CDB data.
    Core analysis of tetrahedral finite elements — nodes + connectivity.
    """

    @staticmethod
    def validate_mesh(nodes_df, elements):
        """
        Validate tetrahedral mesh integrity.
        Checks: node references, element size, degenerate elements, volume.
        """
        if nodes_df.empty or not elements:
            return {'valid': False, 'reason': 'Empty data'}, []

        valid_nids = set(nodes_df['node_id'].astype(int).values)
        nid_to_xyz = {}
        for _, row in nodes_df.iterrows():
            nid_to_xyz[int(row['node_id'])] = np.array([row['x'], row['y'], row['z']])

        results = {
            'total_elements': len(elements), 'valid_elements': 0,
            'invalid_node_refs': 0, 'degenerate': 0, 'negative_volume': 0,
            'nodes_used': set(), 'orphan_nodes': 0,
        }
        valid_elements = []
        for elem in elements:
            if not all(nid in valid_nids for nid in elem[:4]):
                results['invalid_node_refs'] += 1
                continue
            pts = [nid_to_xyz[nid] for nid in elem[:4]]
            v0, v1, v2 = pts[1] - pts[0], pts[2] - pts[0], pts[3] - pts[0]
            det = np.dot(v0, np.cross(v1, v2))
            volume = abs(det) / 6.0
            if volume < 1e-15:
                results['degenerate'] += 1
                continue
            if det < 0:
                results['negative_volume'] += 1
            results['valid_elements'] += 1
            results['nodes_used'].update(elem[:4])
            valid_elements.append(elem)

        results['orphan_nodes'] = len(valid_nids - results['nodes_used'])
        results['nodes_used'] = len(results['nodes_used'])
        results['valid'] = results['valid_elements'] > 0
        return results, valid_elements

    @staticmethod
    def compute_mesh_stats(nodes_df, elements):
        """Compute overall mesh statistics."""
        if nodes_df.empty:
            return {}
        coords = nodes_df[['x', 'y', 'z']].values
        return {
            'num_nodes': len(nodes_df), 'num_elements': len(elements),
            'x_span_mm': coords[:, 0].ptp(), 'y_span_mm': coords[:, 1].ptp(),
            'z_span_mm': coords[:, 2].ptp(),
            'centroid': coords.mean(axis=0),
        }

    @staticmethod
    def build_connectivity(elements):
        """
        Build connectivity graph.
        Returns node-to-element map, element adjacency, face-to-element map.
        """
        node_to_elems = defaultdict(list)
        face_to_elems = defaultdict(list)

        for ei, elem in enumerate(elements):
            nodes = elem[:4]
            for nid in nodes:
                node_to_elems[nid].append(ei)
            faces = [
                frozenset([nodes[0], nodes[1], nodes[2]]),
                frozenset([nodes[0], nodes[1], nodes[3]]),
                frozenset([nodes[0], nodes[2], nodes[3]]),
                frozenset([nodes[1], nodes[2], nodes[3]]),
            ]
            for face in faces:
                face_to_elems[face].append(ei)

        adjacency = defaultdict(set)
        for face, elems in face_to_elems.items():
            if len(elems) == 2:
                adjacency[elems[0]].add(elems[1])
                adjacency[elems[1]].add(elems[0])

        return dict(node_to_elems), dict(adjacency), dict(face_to_elems)


