class MeshRepresentation:
    """
    Convert raw CDB mesh data into fixed-size tensors for AI training.

    From each CDB file we extract:
      - INPUT:  Surface point cloud (n_surface_pts × 3)
      - TARGET: Interior node positions (n_interior_pts × 3)

    The AI learns: given surface geometry → predict where interior nodes go.
    Then scipy Delaunay tetrahedralizes the combined points into a tet mesh.
    """

    @staticmethod
    def normalize_points(points, return_params=True):
        """Center and scale to unit sphere."""
        centroid = points.mean(axis=0)
        centered = points - centroid
        scale = np.max(np.linalg.norm(centered, axis=1))
        if scale < 1e-10:
            scale = 1.0
        normalized = centered / scale
        if return_params:
            return normalized, centroid, scale
        return normalized

    @staticmethod
    def denormalize_points(points, centroid, scale):
        """Reverse normalization."""
        return points * scale + centroid

    @staticmethod
    def sample_or_pad(points, target_n):
        """Resample point cloud to exactly target_n points."""
        n = len(points)
        if n == 0:
            return np.zeros((target_n, 3))
        if n >= target_n:
            idx = np.random.choice(n, target_n, replace=False)
        else:
            idx = np.concatenate([
                np.arange(n),
                np.random.choice(n, target_n - n, replace=True)
            ])
        return points[idx]

    @staticmethod
    def prepare_training_pair(nodes_df, elements):
        """
        Convert one CDB mesh into (surface_pts, interior_pts) pair.

        1. Extract surface and interior nodes
        2. Normalize to unit sphere
        3. Resample to fixed counts
        """
        surface_faces, surface_nids = SurfaceExtractor.extract_surface(elements)
        all_nids = set(nodes_df['node_id'].astype(int).values)
        interior_nids = all_nids - surface_nids

        # Get coordinates
        nid_to_xyz = {}
        for _, row in nodes_df.iterrows():
            nid_to_xyz[int(row['node_id'])] = np.array([row['x'], row['y'], row['z']])

        surface_pts = np.array([nid_to_xyz[n] for n in surface_nids if n in nid_to_xyz])
        interior_pts = np.array([nid_to_xyz[n] for n in interior_nids if n in nid_to_xyz])

        if len(surface_pts) == 0 or len(interior_pts) == 0:
            return None

        # Normalize using surface centroid/scale (both surface and interior)
        all_pts = np.vstack([surface_pts, interior_pts])
        _, centroid, scale = MeshRepresentation.normalize_points(all_pts)
        surface_norm = (surface_pts - centroid) / scale
        interior_norm = (interior_pts - centroid) / scale

        # Resample to fixed size
        surface_fixed = MeshRepresentation.sample_or_pad(
            surface_norm, MODEL_CONFIG['n_surface_pts'])
        interior_fixed = MeshRepresentation.sample_or_pad(
            interior_norm, MODEL_CONFIG['n_interior_pts'])

        return {
            'surface': surface_fixed.astype(np.float32),
            'interior': interior_fixed.astype(np.float32),
            'centroid': centroid, 'scale': scale,
            'n_surface_orig': len(surface_pts),
            'n_interior_orig': len(interior_pts),
        }


