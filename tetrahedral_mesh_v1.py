def chamfer_distance(pred, target):
    """
    Chamfer Distance between two point clouds.
    CD = mean(min_dist(pred→target)) + mean(min_dist(target→pred))
    """
    # pred: (B, N, 3), target: (B, M, 3)
    diff = pred.unsqueeze(2) - target.unsqueeze(1)  # (B, N, M, 3)
    dist = torch.sum(diff ** 2, dim=-1)              # (B, N, M)

    min_pred_to_target = dist.min(dim=2)[0].mean(dim=1)  # (B,)
    min_target_to_pred = dist.min(dim=1)[0].mean(dim=1)  # (B,)

    return (min_pred_to_target + min_target_to_pred).mean()


def earth_movers_distance_approx(pred, target, n_iter=50):
    """
    Approximate EMD via iterative closest point matching.
    More accurate than Chamfer but slower.
    """
    B, N, D = pred.shape
    _, M, _ = target.shape

    # Use Chamfer as approximation when M != N
    if N != M:
        return chamfer_distance(pred, target)

    # Sinkhorn-like approximation
    diff = pred.unsqueeze(2) - target.unsqueeze(1)
    cost = torch.sum(diff ** 2, dim=-1)  # (B, N, M)

    # Greedy assignment approximation
    total = 0
    for b in range(B):
        c = cost[b].clone()
        assignment_cost = 0
        for _ in range(min(N, M)):
            min_val, min_idx = c.min(dim=1)
            row_idx = min_val.argmin()
            col_idx = min_idx[row_idx]
            assignment_cost += c[row_idx, col_idx]
            c[row_idx, :] = float('inf')
            c[:, col_idx] = float('inf')
        total += assignment_cost / min(N, M)
    return total / B


class MeshGenerationLoss(nn.Module):
    """
    Combined loss for mesh generation:
    1. Chamfer Distance (primary — point distribution accuracy)
    2. KL Divergence (VAE regularization — prevents overfitting on 198 samples)
    3. Density Uniformity (encourages evenly distributed interior nodes)
    """

    def __init__(self):
        super().__init__()
        self.kl_weight = MODEL_CONFIG['kl_weight']
        self.quality_weight = MODEL_CONFIG['quality_weight']

    def forward(self, pred, target, mu, logvar):
        # Chamfer Distance
        cd = chamfer_distance(pred, target)

        # KL Divergence
        kl = -0.5 * torch.mean(1 + logvar - mu.pow(2) - logvar.exp())

        # Density uniformity: penalize clustering via nearest-neighbor variance
        diff = pred.unsqueeze(2) - pred.unsqueeze(1)
        self_dist = torch.sum(diff ** 2, dim=-1)
        self_dist = self_dist + torch.eye(pred.size(1), device=pred.device).unsqueeze(0) * 1e6
        nn_dist = self_dist.min(dim=2)[0]  # (B, N)
        density_loss = nn_dist.std(dim=1).mean()

        total = cd + self.kl_weight * kl + self.quality_weight * density_loss

        return total, {'chamfer': cd.item(), 'kl': kl.item(),
                       'density': density_loss.item(), 'total': total.item()}


class MeshGenerationEvaluator:
    """Evaluate generated mesh quality vs ground truth."""

    @staticmethod
    def evaluate_generated_mesh(pred_interior, gt_interior, surface_pts,
                                 centroid, scale):
        """
        Full evaluation of generated interior nodes.
        Combines predicted interior + surface → Delaunay → tet mesh → quality.
        """
        from scipy.spatial import Delaunay

        # Denormalize
        pred_pts = pred_interior * scale + centroid
        gt_pts = gt_interior * scale + centroid
        surf_pts = surface_pts * scale + centroid

        metrics = {}

        # 1. Chamfer Distance (normalized)
        pred_t = torch.tensor(pred_pts).unsqueeze(0).float()
        gt_t = torch.tensor(gt_pts).unsqueeze(0).float()
        metrics['chamfer_distance'] = chamfer_distance(pred_t, gt_t).item()

        # 2. Generate tetrahedral mesh from predicted interior + surface
        all_pts = np.vstack([surf_pts, pred_pts])
        try:
            tri = Delaunay(all_pts)
            gen_elements = [tuple(simplex) for simplex in tri.simplices]
            metrics['generated_elements'] = len(gen_elements)

            # Create nodes_df for quality analysis
            nodes_df = pd.DataFrame({
                'node_id': range(len(all_pts)),
                'x': all_pts[:, 0], 'y': all_pts[:, 1], 'z': all_pts[:, 2]
            })

            # 3. Compute quality metrics on generated mesh
            qdf = MeshQualityAnalyzer.compute_all_metrics(nodes_df, gen_elements)
            if not qdf.empty:
                metrics['mean_aspect_ratio'] = qdf['aspect_ratio'].mean()
                metrics['mean_scaled_jacobian'] = qdf['scaled_jacobian'].mean()
                metrics['mean_skewness'] = qdf['skewness'].mean()
                metrics['pct_good'] = (qdf['quality_class'] == 'good').mean() * 100
                metrics['pct_poor'] = (qdf['quality_class'] == 'poor').mean() * 100
        except Exception as e:
            metrics['delaunay_error'] = str(e)

        return metrics


