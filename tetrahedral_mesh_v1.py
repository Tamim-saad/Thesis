# SECTION 11: MODEL — DGCNN ENCODER (SOTA for surface geometry)
# ============================================================
"""
Architecture choices based on 2024-2025 mesh generation research:

1. DGCNN (Dynamic Graph CNN) encoder — Wang et al. 2019, widely used in 2024
   - Builds dynamic k-NN graph in feature space
   - EdgeConv captures local geometric patterns
   - Better than PointNet (ignores local structure) and PointNet++ (static groups)
   - Chosen because surface mesh has rich local geometric features

2. Conditional VAE decoder — for generative interior node prediction
   - VAE provides regularized latent space (good for 198 samples)
   - Conditional on surface features
   - Generates varied but valid interior distributions

3. DefTet-inspired post-processing — Gao et al. (NVIDIA)
   - Delaunay tetrahedralization of predicted points
   - Quality-aware mesh refinement
"""


def knn(x, k):
    """Compute k-nearest neighbors in feature space."""
    inner = -2 * torch.matmul(x.transpose(2, 1), x)
    xx = torch.sum(x ** 2, dim=1, keepdim=True)
    pairwise_distance = -xx - inner - xx.transpose(2, 1)
    idx = pairwise_distance.topk(k=k, dim=-1)[1]
    return idx


def get_graph_feature(x, k=20, idx=None):
    """Construct edge features for DGCNN."""
    batch_size, num_dims, num_points = x.size()
    device = x.device

    if idx is None:
        idx = knn(x, k=k)

    idx_base = torch.arange(0, batch_size, device=device).view(-1, 1, 1) * num_points
    idx = idx + idx_base
    idx = idx.view(-1)

    x = x.transpose(2, 1).contiguous()
    feature = x.view(batch_size * num_points, -1)[idx, :]
    feature = feature.view(batch_size, num_points, k, num_dims)
    x = x.view(batch_size, num_points, 1, num_dims).repeat(1, 1, k, 1)

    # Edge feature = [neighbor - center, center]
    feature = torch.cat((feature - x, x), dim=3).permute(0, 3, 1, 2).contiguous()
    return feature


class DGCNNEncoder(nn.Module):
    """
    Dynamic Graph CNN encoder for surface point clouds.

    Builds dynamic k-NN graph in feature space at each layer,
    applies EdgeConv to capture local geometric patterns.
    Outputs a global feature vector encoding surface geometry.

    Reference: Wang et al., "Dynamic Graph CNN for Learning on Point Clouds"
    """

    def __init__(self, input_dim=3, output_dim=512, k=20):
        super().__init__()
        self.k = k

        # EdgeConv layers with increasing feature dims
        self.conv1 = nn.Sequential(
            nn.Conv2d(input_dim * 2, 64, 1, bias=False),
            nn.BatchNorm2d(64), nn.LeakyReLU(0.2))
        self.conv2 = nn.Sequential(
            nn.Conv2d(64 * 2, 64, 1, bias=False),
            nn.BatchNorm2d(64), nn.LeakyReLU(0.2))
        self.conv3 = nn.Sequential(
            nn.Conv2d(64 * 2, 128, 1, bias=False),
            nn.BatchNorm2d(128), nn.LeakyReLU(0.2))
        self.conv4 = nn.Sequential(
            nn.Conv2d(128 * 2, 256, 1, bias=False),
            nn.BatchNorm2d(256), nn.LeakyReLU(0.2))

        # Aggregate all EdgeConv outputs
        self.conv5 = nn.Sequential(
            nn.Conv1d(64 + 64 + 128 + 256, output_dim, 1, bias=False),
            nn.BatchNorm1d(output_dim), nn.LeakyReLU(0.2))

    def forward(self, x):
        """
        x: (B, 3, N) surface points
        returns: (B, output_dim) global feature
        """
        batch_size = x.size(0)

        # EdgeConv blocks
        x1 = self.conv1(get_graph_feature(x, k=self.k))
        x1 = x1.max(dim=-1)[0]

        x2 = self.conv2(get_graph_feature(x1, k=self.k))
        x2 = x2.max(dim=-1)[0]

        x3 = self.conv3(get_graph_feature(x2, k=self.k))
        x3 = x3.max(dim=-1)[0]

        x4 = self.conv4(get_graph_feature(x3, k=self.k))
        x4 = x4.max(dim=-1)[0]

        # Concatenate multi-scale features
        x = torch.cat((x1, x2, x3, x4), dim=1)
        x = self.conv5(x)

        # Global max + avg pooling
        x_max = F.adaptive_max_pool1d(x, 1).view(batch_size, -1)
        x_avg = F.adaptive_avg_pool1d(x, 1).view(batch_size, -1)

        return torch.cat((x_max, x_avg), dim=1)  # (B, output_dim * 2)


