# SECTION 6: SURFACE EXTRACTION
# ============================================================
class SurfaceExtractor:
    """
    Extract surface (boundary) mesh from tetrahedral volume mesh.

    Surface = triangular faces belonging to ONLY ONE tetrahedron.
    Internal faces are shared by two tetrahedra.

    In the thesis pipeline:
    - Surface mesh → INPUT to the AI mesh generator
    - Volume mesh  → OUTPUT of the AI mesh generator (ground truth from CDB)
    """

    @staticmethod
    def extract_surface(elements):
        """
        Extract boundary faces.
        Returns: (surface_faces, surface_node_ids)
        """
        face_count = Counter()
        for elem in elements:
            nodes = elem[:4]
            for face in [
                tuple(sorted([nodes[0], nodes[1], nodes[2]])),
                tuple(sorted([nodes[0], nodes[1], nodes[3]])),
                tuple(sorted([nodes[0], nodes[2], nodes[3]])),
                tuple(sorted([nodes[1], nodes[2], nodes[3]])),
            ]:
                face_count[face] += 1

        surface_faces = [f for f, c in face_count.items() if c == 1]
        surface_nodes = set()
        for face in surface_faces:
            surface_nodes.update(face)
        return surface_faces, surface_nodes

    @staticmethod
    def get_surface_mesh(nodes_df, elements):
        """
        Get complete surface mesh data.
        Returns: (surface_nodes_df, surface_faces, stats_dict)
        """
        surface_faces, surface_nids = SurfaceExtractor.extract_surface(elements)
        surface_nodes_df = nodes_df[nodes_df['node_id'].isin(surface_nids)].copy()

        total_faces = len(elements) * 4
        stats = {
            'total_elements': len(elements),
            'surface_faces': len(surface_faces),
            'internal_faces': (total_faces - len(surface_faces)) // 2,
            'total_nodes': len(nodes_df),
            'surface_nodes': len(surface_nids),
            'interior_nodes': len(nodes_df) - len(surface_nids),
        }
        return surface_nodes_df, surface_faces, stats


